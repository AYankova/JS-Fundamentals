function Solve(args) {
    var arr = args[0].split(', ').map(Number),
        countPatterns = parseInt(args[1]),
        patterns = [],
        sum = 0,
        maxSum = Number.NEGATIVE_INFINITY;

    for (var i = 0; i < countPatterns; i += 1) {
        patterns[i] = args[i + 2].split(', ').map(Number);
    }

    var start = 0;
    var isUsed = [];
    isUsed[0] = true;
    sum += arr[0];

    for (i = 0; i < countPatterns; i += 1) {
        var exit = false;
        while (!exit) {
            for (var j = 0; j < patterns[i].length; j += 1) {
                var tmp = patterns[i][j];
                start = start + tmp;
                if (start < arr.length && !isUsed[start]) {
                    sum += arr[start];
                    isUsed[start] = true;
                } else {
                    exit = true;
                    break;
                }
            }
        }
        if (maxSum < sum) {
            maxSum = sum;
        }
        sum = arr[0];
        isUsed = [];
        isUsed[0] = true;
    }

    return maxSum;
}

